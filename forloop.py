# loop, perulangan, itteration
# - for loop
# range ->
#  range(start, stop, step)
#  merupakan variable iterasi yang digunakan untuk parameter perulangan dimulai dari 0

fruits = ["apple", "banana", "cherry"]
for x in fruits:
    print(x)

print("----------------------------------")

# range dimulai 1 di stop di 2 dan tiap perulangan ditambah dengan 2
for n in range(1, 6, 2):
    print(n)

print("----------------------------------")

for n in range(3, 6):
    print(n)